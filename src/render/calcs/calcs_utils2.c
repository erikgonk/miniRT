#include "../../inc/miniRT.h"

uint32_t	texture_weight(t_rgb c1, t_rgb c2)
{
	c1.r *= 0.9;
	c1.g *= 0.9;
	c1.b *= 0.9;
	c2.r *= 0.1;
	c2.g *= 0.1;
	c2.b *= 0.1;
	return (get_colour(color_add(c1, c2)));
}
